def get_text(key, lang_code="en"):
    """
    Get translated text for a given key and language code.
    
    Parameters:
    -----------
    key : str
        Key for the text to be translated
    lang_code : str
        Language code (default: "en" for English)
        
    Returns:
    --------
    str
        Translated text
    """
    # If the key or language is not found, return the English text or key itself
    if key not in TRANSLATIONS:
        return key
    
    if lang_code not in TRANSLATIONS[key]:
        return TRANSLATIONS[key]["en"]
    
    return TRANSLATIONS[key][lang_code]

def get_language_name(lang_code):
    """
    Get language name for a given language code.
    
    Parameters:
    -----------
    lang_code : str
        Language code
        
    Returns:
    --------
    str
        Language name
    """
    if lang_code in LANGUAGES:
        return LANGUAGES[lang_code]
    return "Unknown"